<script setup lang="ts">
import { computed } from 'vue'
import { useRoute } from 'vue-router'
import { useAuthStore } from '@/stores/auth'

const route = useRoute()
const authStore = useAuthStore()

// All nav items with role requirements
const allNavItems = [
  { name: 'Projects', path: '/', icon: 'folder', requiresManager: false },
  { name: 'PM Review', path: '/pm-review', icon: 'clipboard', requiresManager: true },
  { name: 'Templates', path: '/templates', icon: 'template', requiresManager: false },
  { name: 'Users', path: '/users', icon: 'users', requiresManager: true },
]

// Filter nav items based on user role
const navItems = computed(() => {
  return allNavItems.filter(item => {
    if (item.requiresManager) {
      return authStore.isManager
    }
    return true
  })
})

const isActive = (path: string) => {
  if (path === '/') {
    return route.path === '/' || route.path.startsWith('/project/')
  }
  return route.path === path
}
</script>

